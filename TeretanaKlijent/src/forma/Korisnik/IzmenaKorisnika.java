    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        Korisnik korisnik = (Korisnik) sesija.Sesija.getInstance().getObjekat();
        korisnik.setImePrezime(txtImePrezime.getText());
        korisnik.setTelefon(txtTelefon.getText());
        korisnik.seteMail(txtEmail.getText());

        try {

            kontroler.Kontroler.getInstance().izmeniKorisnika(korisnik);
            JOptionPane.showMessageDialog(this, "Uspe≈°no ste izmenili korisnika.");

            PretragaKorisnika pk = (PretragaKorisnika) SwingUtilities.getWindowAncestor(this).getParent();
            pk.getTmk().isprazniTabelu();
            pk.getTxtID().setText("");
            pk.getTxtImePrezime().setText("");
            pk.getTxtRadnikKojiJeUneo().setText("");
            SwingUtilities.getWindowAncestor(this).dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    public void prikazi() {
        Korisnik k = (Korisnik) sesija.Sesija.getInstance().getObjekat();
        txtID.setText("" + k.getKorisnikID());
        txtImePrezime.setText(k.getImePrezime());
        txtTelefon.setText(k.getTelefon());
        txtEmail.setText(k.geteMail());
        txtUneoRadnik.setText(k.getRadnik() + "");
        txtID.setEnabled(false);
        txtUneoRadnik.setEnabled(false);
    }

