    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String id = txtID.getText();
        String imePrezime = txtImePrezime.getText();
        String radnikUneo = txtRadnikKojiJeUneo.getText();
        if (id.isEmpty() && imePrezime.isEmpty() && radnikUneo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli ni jedan parametar pretrage.");
            return;
        }
        try {
            tmk.setKorisnici(kontroler.Kontroler.getInstance().pretraziKorisnike(id, imePrezime, radnikUneo));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
            return;
        }

    }//GEN-LAST:event_btnPretraziActionPerformed

