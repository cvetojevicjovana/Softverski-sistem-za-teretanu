    public void popuniSesiju(int selektovanRed) {
        int id = (int) tabelaKorisnici.getValueAt(selektovanRed, 0);
        String ime = (String) tabelaKorisnici.getValueAt(selektovanRed, 1);
        String email = (String) tabelaKorisnici.getValueAt(selektovanRed, 2);
        String telefon = (String) tabelaKorisnici.getValueAt(selektovanRed, 3);
        Radnik r = (Radnik) tabelaKorisnici.getValueAt(selektovanRed, 4);
        Korisnik k = new Korisnik(id, ime, email, telefon, r);
        sesija.Sesija.getInstance().setObjekat(null);
        sesija.Sesija.getInstance().setObjekat(k);
    }

