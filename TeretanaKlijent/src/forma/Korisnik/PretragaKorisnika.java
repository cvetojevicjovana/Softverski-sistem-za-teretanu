    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        int selektovanRed = tabelaKorisnici.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Morate selektovati red.");
            return;
        }
        popuniSesiju(selektovanRed);
        JDialog dijalog = new JDialog(PretragaKorisnika.this, "[TERETANA] Prikaz korisnika", true);
        PrikazKorisnika pk = new PrikazKorisnika();
        dijalog.add(pk);
        dijalog.setSize(420, 200);
        dijalog.setLocationRelativeTo(this);
        dijalog.pack();
        dijalog.setVisible(true);
    }//GEN-LAST:event_btnPrikaziActionPerformed

