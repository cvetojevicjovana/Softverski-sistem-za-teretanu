    private void btnPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretragaActionPerformed
        //String jed = (String) cmbxKorisnik.getSelectedItem();
        //String jed2 = (String) cmbxStatus.getSelectedItem();
        //String jed3 = (String) cmbxTreninzi.getSelectedItem();

        Korisnik kor = null;
        Trening tr = null;
        String status = "";

        if (cmbxKorisnik.getSelectedItem().equals("")) {
            if (cmbxStatus.getSelectedItem().equals("")) {
                if (cmbxTreninzi.getSelectedItem().equals("")) {
                    JOptionPane.showMessageDialog(this, "Morate odabrati barem jedan kriterijum.");
                    return;
                }
            }
        }

        if (!cmbxKorisnik.getSelectedItem().equals("")) {
            kor = (Korisnik) cmbxKorisnik.getSelectedItem();
        }
        if (!cmbxStatus.getSelectedItem().equals("")) {
            status = (String) cmbxStatus.getSelectedItem();
        }
        if (!cmbxTreninzi.getSelectedItem().equals("")) {
            tr = (Trening) cmbxTreninzi.getSelectedItem();
        }

        try {
            tmot.setOdTreninzi(kontroler.Kontroler.getInstance().pretraziOdabraneTreninge(kor, tr, status));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }
    }//GEN-LAST:event_btnPretragaActionPerformed

