    private void popuniSesiju(int selektovanRed) {
        OdabraniTrening ot = tmot.getOdTreninzi().get(selektovanRed);
        sesija.Sesija.getInstance().setObjekat(null);
        sesija.Sesija.getInstance().setObjekat(ot);
    }

    private void inicijalizujKorisnike() {
        try {
            ArrayList<Korisnik> listaKorisnika = kontroler.Kontroler.getInstance().vratiSveKorisnike();
            cmbxKorisnik.addItem("");

            for (Korisnik korisnik : listaKorisnika) {
                cmbxKorisnik.addItem(korisnik);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "GRESKA - korisnici");
            return;
        }
    }

    private void inizijalizujTreninge() {
        try {
            ArrayList<Trening> listaTreninga = kontroler.Kontroler.getInstance().vratiSveTreninge();
            cmbxTreninzi.addItem("");
            for (Trening trening : listaTreninga) {
                cmbxTreninzi.addItem(trening);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "GRESKA - treninzi");
            return;
        }
    }

    private void inizijalizujStatus() {
        cmbxStatus.addItem("");
        cmbxStatus.addItem("Važeći");
        cmbxStatus.addItem("Nevažeći");
    }

