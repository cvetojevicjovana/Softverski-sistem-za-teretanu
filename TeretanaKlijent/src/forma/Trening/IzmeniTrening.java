    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        Trening trening = (Trening) sesija.Sesija.getInstance().getObjekat();
        trening.setNaziv(txtNaziv.getText());
        trening.setOpis(txtOpis.getText());
        try {
            String cenaTxt = txtCena.getText();
            double cena = Double.parseDouble(cenaTxt);
            trening.setCena(cena);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "U polje cena morate uneti broj.");
            return;
        }
        try {
            String brTreningaTxt = txtBrojTermina.getText();
            int broj = Integer.parseInt(brTreningaTxt);
            trening.setBrojTermina(broj);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "U polje broj termina morate uneti broj.");
            return;
        }
        try {
            kontroler.Kontroler.getInstance().izmeniTrening(trening);
            JOptionPane.showMessageDialog(this, "Uspe≈°no ste izmenili trening.");

            PretragaTreninga pt = (PretragaTreninga) SwingUtilities.getWindowAncestor(this).getParent();
            pt.getTmt().isprazniTabelu();
            pt.getTxtID().setText("");
            pt.getTxtNaziv().setText("");
            pt.getTxtCena().setText("");
            pt.getTxtOpis().setText("");
            SwingUtilities.getWindowAncestor(this).dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed


