    private void prikazi() {
        Trening t = (Trening) sesija.Sesija.getInstance().getObjekat();
        txtID.setText(t.getTreningID() + "");
        txtNaziv.setText(t.getNaziv());
        txtOpis.setText(t.getOpis());
        txtBrojTermina.setText(t.getBrojTermina() + "");
        txtCena.setText(t.getCena() + "");
        txtID.setEnabled(false);
    }
